#!/bin/bash
# install.sh — install singularity-sandbox into a target git repo.
#
# Usage:
#   bash install.sh [--target <repo-dir>] [--with-claude-code]
#
# By default installs only the core commands + a pre-merge-commit git hook that
# runs sandbox-merge-gate. With --with-claude-code, also installs the Claude
# Code adapter hooks and a settings-hooks.json snippet (auto-merged if jq
# is available, otherwise printed for manual merge).

set -u

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
TARGET=""
WITH_CC=0

while [ $# -gt 0 ]; do
  case "$1" in
    --target)           TARGET="$2"; shift 2 ;;
    --with-claude-code) WITH_CC=1; shift ;;
    -h|--help)
      sed -n '2,11p' "$0" | sed 's/^# \{0,1\}//'
      exit 0
      ;;
    *) printf 'unknown arg: %s\n' "$1" >&2; exit 2 ;;
  esac
done

[ -z "$TARGET" ] && TARGET="$(pwd)"
[ -d "$TARGET/.git" ] || { printf 'not a git repo: %s\n' "$TARGET" >&2; exit 1; }

INSTALL_ROOT="$TARGET/.sandbox"
mkdir -p "$INSTALL_ROOT/core/lib" "$INSTALL_ROOT/core/cmd"

# sync_sh_dir <src_dir> <dest_dir> — mirror *.sh from src into dest.
# Wipes any *.sh files already in dest so that files renamed or deleted in
# the source propagate to the install target on re-run. Other contents of
# dest (runtime state, worktrees, markers) are never touched because we
# glob on *.sh only and never the directory itself. Copy failures are
# fatal — a partial install is worse than a clean abort.
sync_sh_dir() {
  local src="$1" dest="$2"
  # Validate source BEFORE wiping dest — a broken source tree must abort
  # without destroying the previously-installed files.
  # shellcheck disable=SC2206
  local files=( "$src"/*.sh )
  if [ ! -e "${files[0]}" ]; then
    printf 'install: no *.sh files in %s\n' "$src" >&2
    exit 1
  fi
  rm -f "$dest"/*.sh
  cp "${files[@]}" "$dest/" || {
    printf 'install: copy failed %s -> %s\n' "$src" "$dest" >&2
    exit 1
  }
}

printf '[install] copying core to %s\n' "$INSTALL_ROOT/core"
sync_sh_dir "$SCRIPT_DIR/core/lib" "$INSTALL_ROOT/core/lib"
sync_sh_dir "$SCRIPT_DIR/core/cmd" "$INSTALL_ROOT/core/cmd"
chmod +x "$INSTALL_ROOT/core/cmd/"*.sh

GIT_COMMON=$(git -C "$TARGET" rev-parse --git-common-dir 2>/dev/null)
case "$GIT_COMMON" in
  /*|[A-Za-z]:*) ;;
  *) GIT_COMMON="$TARGET/$GIT_COMMON" ;;
esac
HOOK="$GIT_COMMON/hooks/pre-merge-commit"
printf '[install] writing pre-merge-commit hook at %s\n' "$HOOK"
mkdir -p "$(dirname "$HOOK")"
cat > "$HOOK" << 'HOOK_EOF'
#!/bin/bash
# pre-merge-commit — generated by singularity-sandbox install.sh
#
# Git fires this hook on the TARGET side of a non-ff merge, BEFORE it
# writes .git/MERGE_HEAD. The merge tree is already staged in the target's
# index, so naively scanning $REPO_ROOT for cleanliness would flag the
# legitimate merge content as dirty and block every non-ff sandbox merge.
#
# The gate's real job is to validate the BRANCH being merged — its
# TASK.md completion and worktree state — not the target that is about
# to receive it. Since MERGE_HEAD does not exist yet at this point,
# `git rev-parse MERGE_HEAD` fails, and we use the environment instead:
# git exports `GITHEAD_<sha>=<branch-name>` for each head being merged.
# We collect those SHAs, walk linked worktrees, and gate the first
# sandbox worktree whose HEAD matches.
#
# Fail-open when: no sandbox worktree matches, .sandbox/ is not installed,
# non-sandbox merge, already-reaped sandbox. Gates are safety nets.
set -u
REPO_ROOT="$(git rev-parse --show-toplevel 2>/dev/null)" || exit 0
GATE="$REPO_ROOT/.sandbox/core/cmd/sandbox-merge-gate.sh"
[ -x "$GATE" ] || exit 0

# git exports GIT_INDEX_FILE / GIT_DIR / GIT_WORK_TREE / GIT_PREFIX into the
# hook environment so that any `git` invocation runs against the in-progress
# merge's index. Those vars must be unset before we inspect the SANDBOX
# worktree, otherwise `git -C <sandbox> status` reads main's merge-staged
# index and reports false dirt on the sandbox.
unset GIT_INDEX_FILE GIT_DIR GIT_WORK_TREE GIT_PREFIX

# Collect SHAs of all branches being merged from GITHEAD_<sha> env vars.
MERGE_SHAS=""
while IFS='=' read -r _name _val; do
  case "$_name" in
    GITHEAD_*) MERGE_SHAS="$MERGE_SHAS ${_name#GITHEAD_} " ;;
  esac
done < <(env)
[ -n "$MERGE_SHAS" ] || exit 0

# Walk linked worktrees; find the first non-main worktree whose HEAD
# matches one of the SHAs being merged.
SANDBOX_WT=""
cur_wt=""
while IFS= read -r _line; do
  case "$_line" in
    "worktree "*) cur_wt="${_line#worktree }" ;;
    "HEAD "*)
      _sha="${_line#HEAD }"
      case "$MERGE_SHAS" in
        *" $_sha "*)
          if [ -n "$cur_wt" ] && [ "$cur_wt" != "$REPO_ROOT" ]; then
            SANDBOX_WT="$cur_wt"
            break
          fi
          ;;
      esac
      ;;
  esac
done < <(git worktree list --porcelain 2>/dev/null)

[ -n "$SANDBOX_WT" ] || exit 0
[ -d "$SANDBOX_WT" ] || exit 0

if ! out=$(bash "$GATE" --worktree "$SANDBOX_WT" 2>&1); then
  printf '%s\n' "$out" >&2
  exit 1
fi
exit 0
HOOK_EOF
chmod +x "$HOOK"

# post-merge hook — re-syncs .sandbox/ core+adapter after every merge into main.
# This ensures that code changes committed in a sandbox branch are immediately
# reflected in the installed .sandbox/ copy, preventing stale-file drift.
POST_MERGE="$GIT_COMMON/hooks/post-merge"
printf '[install] writing post-merge hook at %s\n' "$POST_MERGE"
cat > "$POST_MERGE" << 'HOOK_EOF'
#!/bin/bash
# post-merge — generated by singularity-sandbox install.sh
# Re-runs install.sh after every merge so .sandbox/ stays in sync with source.
# Fail-open: errors here must never block the merge that already succeeded.
REPO_ROOT="$(git rev-parse --show-toplevel 2>/dev/null)" || exit 0
INSTALLER="$REPO_ROOT/install.sh"
[ -f "$INSTALLER" ] || exit 0

# Detect whether Claude Code adapter is installed; if so, pass --with-claude-code.
_cc_flag=""
[ -d "$REPO_ROOT/.sandbox/adapter" ] && _cc_flag="--with-claude-code"

# Run in background so merge returns instantly; suppress output unless error.
( bash "$INSTALLER" --target "$REPO_ROOT" $_cc_flag >/dev/null 2>&1 || \
  printf '[sandbox] post-merge: install.sh failed (exit %s)\n' "$?" >&2 ) &
exit 0
HOOK_EOF
chmod +x "$POST_MERGE"

if [ "$WITH_CC" -eq 1 ]; then
  ADAPTER_DIR="$INSTALL_ROOT/adapter"
  mkdir -p "$ADAPTER_DIR/hooks" "$ADAPTER_DIR/lib"
  printf '[install] copying Claude Code adapter to %s\n' "$ADAPTER_DIR"
  sync_sh_dir "$SCRIPT_DIR/adapters/claude-code/lib"   "$ADAPTER_DIR/lib"
  sync_sh_dir "$SCRIPT_DIR/adapters/claude-code/hooks" "$ADAPTER_DIR/hooks"
  chmod +x "$ADAPTER_DIR/hooks/"*.sh

  SNIPPET="$SCRIPT_DIR/adapters/claude-code/settings-hooks.json"
  SETTINGS="$TARGET/.claude/settings.json"
  mkdir -p "$TARGET/.claude"

  if [ ! -f "$SETTINGS" ]; then
    printf '[install] creating %s\n' "$SETTINGS"
    cp "$SNIPPET" "$SETTINGS"
  elif command -v jq >/dev/null 2>&1; then
    printf '[install] merging hooks into %s via jq\n' "$SETTINGS"
    tmp=$(mktemp)
    jq -s ".[0] * .[1]" "$SETTINGS" "$SNIPPET" > "$tmp" && mv "$tmp" "$SETTINGS"
  else
    printf '[install] jq not found — manual merge required.\n' >&2
    printf '  merge this snippet into %s:\n\n' "$SETTINGS" >&2
    cat "$SNIPPET" >&2
  fi
fi

printf '[install] done.\n'
printf '  core installed at: %s\n' "$INSTALL_ROOT/core"
printf '  pre-merge-commit hook: %s\n' "$HOOK"
printf '  post-merge hook: %s\n' "$POST_MERGE"
[ "$WITH_CC" -eq 1 ] && printf '  claude-code adapter: %s\n' "$INSTALL_ROOT/adapter"
